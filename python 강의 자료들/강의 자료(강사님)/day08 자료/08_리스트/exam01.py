"""
파이썬의 리스트(List)는 가변적이고 순차적인 데이터 구조
여러 값들을 저장하고 관리
리스트를 만들 때는 []를 사용
리스트의 각 요소는 ,로 구분
"""

# 리스트 생성하기 및 초기화
empty_list = []  # 빈 리스트 생성, list()
numbers = [1, 2, 3, 4, 5]  # 정수형 리스트 생성
fruits = ["apple", "banana", "cherry"]  # 문자열 리스트 생성
mixed_list = [1, "apple", 3.14]  # 다양한 자료형이 포함된 리스트 생성

print(empty_list)
print(numbers)
print(fruits)
print(mixed_list)